((w,n)=>{
	let ref,e=x=>x.replace(/[&'\\#<>`*~_=:"!\[\]()\n\t-]/g,m=>`&#${m.charCodeAt(0)};`),r=[
		[/\r\n?/g,'\n'],
		[/\n\[([^\[\]]+?)\]:\s+(?:<\s*([^>]*)\s*>|(\S+))(?:\s+(?:'([^']*)'|"([^"]*)"|\(([^)]*)\)|(\S+)))?$/gm,(_,n,a,b,c,d,e,f)=>(ref[n]=[a||b,c||d||e||f],'')],
		[/\n\n```\n([^]*?)\n```(?=\n\n)/g,(_,m)=>`\n\n<pre>${e(m)}</pre>`],
		[/(`+)((\n?.+?)*?(\n?[^`\n]|.\n))\1(?!`)/g,(_,m,n)=>`<tt>${e(n.replace(/^(\s)(.*)\1$/,'$2'))}</tt>`],
		[/\\[\x21-\x2f:;<=>?@[\\\]^_`{|}~\n]/g,m=>m=='\\\n'?'<br>':`&#${m.charCodeAt(1)};`],
		[/\n\n([-_*][^\S\n]*){3,}(?=\n\n)/g,'\n\n<hr>'],
		[/\n\n(#{1,6})[^\S\n]+(\S.*?)([^\S\n]+#+)?(?=\n\n)/g,(_,m,n)=>`\n\n<h${m.length}>${n.trim()}</h${m.length}>`],
		[/\n\n(.+(\n.+)*)\n=+(?=\n\n)/g,'\n\n<h1>$1</h1>'],
		[/\n\n(.+(\n.+)*)\n-+(?=\n\n)/g,'\n\n<h2>$1</h2>'],
		[/\n\>[^\S\n]*(.*)/g,'\n\n<blockquote>\n\n$1\n\n</blockquote>\n'],
		[/\n\n<\/(blockquote)>\n\n\n<\1>\n\n/g,'\n'],
		[/\n[-+*][^\S\n]+(.*)/g,'\n\n<ul><li>$1</li></ul>\n'],
		[/\n\d+[.)][^\S\n]*(.*)/g,'\n\n<ol><li>$1</li></ol>\n'],
		[/<\/([ou]l)>\n\n\n<\1>/g,''],
		[/___(\n?(.+\n)*?.+?\n?)___/g,'<u>$1</u>'],
		[/(\*\*|__)(\n?(.+\n)*?.+?\n?)\1/g,'<b>$2</b>'],
		[/([*_])(?!\1)(\n?(.+\n)*?.+?\n?)\1/g,'<i>$2</i>'],
		[/~~(\n?(.+\n)*?.+?\n?)~~/g,'<s>$1</s>'],
		[/:"(\n?(.+\n)*?.+?\n?)":/g,'<q>$1</q>'],
		[/\!\[([^\[]+?)\]\s*\(([^\)]+?)\)/g,'<img src="$2" alt="$1">'],
		[/\[([^\[]+?)\](?:\s*\[([^\)]+?)\])?/g,(w,t,n)=>(n=n||t,ref[n]?`<a href="${ref[n][0]}" title="${ref[n][1]||''}">${t}</a>`:w)],
		[/\[([^\[]+?)\]\s*\(([^\)]+?)\)/g,'<a href="$2">$1</a>'],
		[/\n\n(.+(\n.+)*)(?=\n\n)/g,(w,m)=>/^\<(\/|blockquote|h\d|hr|li|ol|ul|p|pre|table)\b/.test(m)?w:`\n\n<p>${m}</p>`]]
	w[n]={
		addRule:(p,s)=>r.push([RegExp(p,'g'),s]),
		render:x=>(ref={},r.reduce((x,[p,s])=>x.replace(p,s),`\n\n${x}\n\n`).trim())}
})(self,'Baremark')
